<p>This website contains are some demos I am making while playing around with <a href="https://svelteplot.dev/">SveltePlot</a>.</p>

<p>SveltePlot is an awesome library for Svelte that builds on Observable Plot (and subsequently d3.js)</p>

<p>It builds on the ideas of a <a href="https://vita.had.co.nz/papers/layered-grammar.pdf">layered grammar of graphics</a>, but combines it with the <a href="https://svelte.dev/">Svelte</a> way. SveltePlot was announced at the 2025 <a href="https://www.sveltesummit.com/">Svelte Summit</a> in Barcelona.</p>

